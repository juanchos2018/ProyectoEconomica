private void Registrar(String codigo, String descripcion, int cantidad, double precio) {
      //  throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    obnegproducto = new ClsNegProducto();
     if (obnegproducto.Existe(codigo)) {
         JOptionPane.showMessageDialog(null, "ya Existe esete codigo", "No", 1);
         return;
     }
     
      objproducto = new ClsProducto();
      objproducto.setIdproducto(codigo);    
     objproducto.setDescripcion(descripcion);
     objproducto.setPrecio(precio);
     objproducto.setCantidad(cantidad);
     obnegproducto.Registar(objproducto);     
     JOptionPane.showMessageDialog(null, "Registado We", "Yes", 1);
              
 }
